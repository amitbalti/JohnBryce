class Cake {
  private flour: number; // קמח
  private egg: number; // ביצים
  private milk: number; // חלב
  private chocolate: number; // שוקולד

  constructor(flour: number, egg: number, milk: number, chocolate: number) {
    this.flour = flour;
    this.egg = egg;
    this.milk = milk;
    this.chocolate = chocolate;
  }
}

export default Cake;
